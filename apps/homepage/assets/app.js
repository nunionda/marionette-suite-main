let currentLang = sessionStorage.getItem('lang') || 'ko';

function applyTranslations(lang) {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[key] && translations[key][lang]) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = translations[key][lang];
      } else {
        el.innerHTML = translations[key][lang];
      }
    }
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (translations[key] && translations[key][lang]) {
      el.placeholder = translations[key][lang];
    }
  });
  document.documentElement.lang = lang;
  document.body.setAttribute('data-lang', lang);
}

applyTranslations(currentLang);

const langToggle = document.getElementById('langToggle');
langToggle.addEventListener('click', () => {
  currentLang = currentLang === 'ko' ? 'en' : 'ko';
  sessionStorage.setItem('lang', currentLang);
  applyTranslations(currentLang);
});

function initLibraryGate() {
  const hasAccess = localStorage.getItem('investorLibraryAccess');
  const form = document.getElementById('libraryForm');
  const links = document.getElementById('libraryLinks');
  const fallback = document.getElementById('libraryFallback');
  const submitBtn = document.getElementById('librarySubmit');

  if (!('localStorage' in window)) {
    form.style.display = 'none';
    fallback.style.display = 'block';
    return;
  }

  if (hasAccess) {
    form.style.display = 'none';
    links.style.display = 'block';
  }

  submitBtn.addEventListener('click', () => {
    const emailInput = document.getElementById('libraryEmail');
    if (emailInput.checkValidity()) {
      localStorage.setItem('investorLibraryAccess', emailInput.value);
      form.style.display = 'none';
      links.style.display = 'block';
    } else {
      emailInput.reportValidity();
    }
  });
}

function initContactForm() {
  const form = document.getElementById('contactForm');
  if (!form) return;
  form.addEventListener('submit', (e) => {
    e.preventDefault();
    const name = document.getElementById('contactName').value;
    const email = document.getElementById('contactEmail').value;
    const message = document.getElementById('contactMessage').value;
    const subject = encodeURIComponent(currentLang === 'ko' ? '[Marionette Studios] 투자/제휴 문의' : '[Marionette Studios] Investment/Partnership Inquiry');
    const body = encodeURIComponent(
      `Name: ${name}\nEmail: ${email}\n\n${message}`
    );
    window.location.href = `mailto:invest@marionette-studios.com?subject=${subject}&body=${body}`;
  });
}

document.addEventListener('DOMContentLoaded', () => {
  initLibraryGate();
  initContactForm();
  
  // Filmography tabs
  const tabBtns = document.querySelectorAll('.tab-btn');
  const tabPanels = document.querySelectorAll('.tab-panel');
  
  tabBtns.forEach(btn => {
    btn.addEventListener('click', () => {
      const tabId = btn.getAttribute('data-tab');
      
      tabBtns.forEach(b => b.classList.remove('active'));
      tabPanels.forEach(p => p.classList.remove('active'));
      
      btn.classList.add('active');
      document.getElementById(tabId).classList.add('active');
    });
  });
});
